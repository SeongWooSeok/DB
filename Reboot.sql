-- 사원정보 INFO (개인정보관련메뉴)
CREATE TABLE INFO(
ID VARCHAR2(10) PRIMARY KEY, --사번
DEPTN NUMBER NOT NULL,  -- 부서명
NAME VARCHAR2(20) NOT NULL, -- 팀원이름
PASS VARCHAR2(20) DEFAULT 0000 , --사용자 비밀번호(디폴트 값 :0000)
GRADE NUMBER NOT NULL, --직급(0:팀장 1:팀원 9:퇴사자)

IN_D DATE DEFAULT SYSDATE NOT NULL,--입사일
OUT_D DATE DEFAULT NULL, --퇴사일
BDAY DATE, --생년월일
PNUM NUMBER,--연락처
AD VARCHAR2(50) --주소
);

-- 근태현황 (캘린더)
CREATE TABLE AM( --Attendance Management
ID VARCHAR2(10) PRIMARY KEY, --사번
DEPTN NUMBER NOT NULL,  --부서명
NAME VARCHAR2(20) NOT NULL, --팀원이름
OFF_C NUMBER NOT NULL, --남은 연차횟수

ABS_D DATE, --결근날
LATE_D  DATE, --지각날
OFF_D DATE, --연차사용날
WORK_D NUMBER, --근무일수(출근한날)

START_T DATE DEFAULT SYSDATE, --출근 시간
END_T DATE DEFAULT SYSDATE --퇴근 시간
);

DROP TABLE AM;

--전자결재
CREATE TABLE PM( --PAYMENT MANAGEMENT
ID VARCHAR2(10) PRIMARY KEY, --사번
DEPTN NUMBER NOT NULL,  --부서명
NAME VARCHAR2(20) NOT NULL, --팀원이름
GRADE NUMBER NOT NULL, --직급 (0:팀장 1:팀원 9:퇴사자)
PNUM NUMBER,-- 연락처

TITLE VARCHAR2(100),--제목
CONTENT VARCHAR2(3500),--내용
STATE NUMBER DEFAULT 0 --상태(해야함:0 대기:1 승인:2 반려:3)
);

